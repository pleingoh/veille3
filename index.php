        <p>
            <h3>I.	C’est quoi un DBMS?</h3>
            Un système de gestion de base de données en français (abr. SGBD) est un logiciel système servant à stocker, à manipuler ou gérer, et à partager des données dans une base de données, en garantissant la qualité, la pérennité et la confidentialité des informations, tout en cachant la complexité des opérations.
        </p>
        <p>
            <h3>II.	Quelques définitions ?</h3>
            Nous ne pouvons continuer sans définir certains termes
            <h4> -	Système de gestion   </h4>
            Un système de gestion (management) est un ensemble d'éléments corrélés ou interactifs permettant à un organisme d'établir une politique, des objectifs et d'atteindre ces objectifs.
            <h4>-	Base de données </h4>
            Ensemble d'informations structurées accessibles au moyen d'un logiciel.
            <h4>-	Logiciel système</h4>
            Le logiciel système est un ensemble de programmes informatiques et de bibliothèques logicielles qui fournit un environnement permettant de créer et d'exécuter des logiciels applicatifs.
        </p>
        <p> <h4>Un SGBD est un logiciel ou ensemble de programme permettant la gestion d’informations ou de données concernant une entité mis en relation de manière structurée.</h4> </p>
        <p>
            <h3>III.	Exemple de SGBD</h3>
            Ils existent plusieurs SGBD certains avec de forte communauté et d’autres moins connus
